    private boolean validacijaAktivnosti(String naziv, String opis, VrstaAktivnosti vakt) {
        boolean nazivReg = naziv.matches("^[A-Z][a-zA-Z ]*$");
        boolean opisReg = !opis.isEmpty();
        boolean vaktReg = vakt != null;

        if (nazivReg && opisReg && vaktReg) {
            return true;
        }

        if (!nazivReg) {
            jTextFieldNazivAkt.setBorder(new LineBorder(Color.RED, 3));
        }
        if (!opisReg) {
            jTextFieldOpisAkt.setBorder(new LineBorder(Color.RED, 3));
        }
        if (!vaktReg) {
            jComboBoxVAktivnosti.setBorder(new LineBorder(Color.RED, 3));
        }

        return false;
    }

